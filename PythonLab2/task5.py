import re


def find_specific_words():
    text = input("Введите текст для анализа: ")

    if not text.strip():
        print("Текст не введен.")
        return

    # \b          - граница слова (чтобы не выхватывать куски из середины других слов)
    # [A-Z]       - первая буква должна быть большой латинской (от A до Z)
    # [a-zA-Z]*   - далее может идти любое количество (даже ноль) латинских букв любого регистра
    # (?:\d{2}|\d{4}) - в конце должно быть ЛИБО ровно 2 цифры, ЛИБО ровно 4 цифры (незахватывающая группа)
    # \b          - конец слова (чтобы после цифр не было других букв или цифр)
    pattern = re.compile(r'\b[A-Z][a-zA-Z]*(?:\d{2}|\d{4})\b')

    matches = pattern.findall(text)

    print("\n--- Результат поиска ---")
    if matches:
        print(f"Найдено подходящих слов: {len(matches)}")
        for i, word in enumerate(matches, 1):
            print(f"{i}. {word}")
    else:
        print("Слов, соответствующих заданному формату, не найдено.")
    print("-" * 24)


if __name__ == "__main__":
    find_specific_words()